CREATE TABLE expense_record
(
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id           BIGINT         NOT NULL,
    category_id       BIGINT         NOT NULL,
    batch_id          BIGINT,
    item_name         VARCHAR(120)   NOT NULL,
    merchant          VARCHAR(120),
    amount            NUMERIC(14, 2) NOT NULL,
    currency          VARCHAR(3)     NOT NULL DEFAULT 'CNY',
    quantity          NUMERIC(10, 2),
    unit              VARCHAR(30),
    occurred_at       TIMESTAMPTZ    NOT NULL,
    note              VARCHAR(500),
    source            VARCHAR(20)    NOT NULL,
    raw_text          TEXT,
    ai_confidence     NUMERIC(5, 4),
    anomaly_level     VARCHAR(20)    NOT NULL DEFAULT 'NONE',
    anomaly_score     NUMERIC(5, 4),
    anomaly_message   VARCHAR(500),
    anomaly_confirmed BOOLEAN        NOT NULL DEFAULT FALSE,
    created_at        TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at        TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_expense_record_user
        FOREIGN KEY (user_id)
            REFERENCES app_user (id)
            ON DELETE CASCADE,

    CONSTRAINT fk_expense_record_category
        FOREIGN KEY (category_id)
            REFERENCES category (id)
            ON DELETE RESTRICT,

    CONSTRAINT fk_expense_record_batch
        FOREIGN KEY (batch_id)
            REFERENCES record_batch (id)
            ON DELETE SET NULL,

    CONSTRAINT ck_expense_record_item_name
        CHECK (BTRIM(item_name) <> ''),

    CONSTRAINT ck_expense_record_amount
        CHECK (amount > 0 AND amount <= 99999999.99),

    CONSTRAINT ck_expense_record_currency
        CHECK (currency ~ '^[A-Z]{3}$'),

    CONSTRAINT ck_expense_record_quantity
        CHECK (quantity IS NULL OR quantity > 0),

    CONSTRAINT ck_expense_record_source
        CHECK (
            source IN (
                'MANUAL',
                'TEXT',
                'VOICE',
                'AI',
                'IMPORT'
            )
        ),

    CONSTRAINT ck_expense_record_confidence
        CHECK (
            ai_confidence IS NULL
            OR ai_confidence BETWEEN 0 AND 1
        ),

    CONSTRAINT ck_expense_record_anomaly_level
        CHECK (
            anomaly_level IN (
                'NONE',
                'NOTICE',
                'WARNING'
            )
        ),

    CONSTRAINT ck_expense_record_anomaly_score
        CHECK (
            anomaly_score IS NULL
            OR anomaly_score BETWEEN 0 AND 1
        )
);


CREATE TABLE budget_plan
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id      BIGINT         NOT NULL,
    category_id  BIGINT,
    budget_month DATE           NOT NULL,
    limit_amount NUMERIC(14, 2) NOT NULL,
    alert_ratio  NUMERIC(5, 4)  NOT NULL DEFAULT 0.8000,
    created_at   TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at   TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_budget_plan_user
        FOREIGN KEY (user_id)
            REFERENCES app_user (id)
            ON DELETE CASCADE,

    CONSTRAINT fk_budget_plan_category
        FOREIGN KEY (category_id)
            REFERENCES category (id)
            ON DELETE RESTRICT,

    CONSTRAINT ck_budget_plan_month
        CHECK (EXTRACT(DAY FROM budget_month) = 1),

    CONSTRAINT ck_budget_plan_amount
        CHECK (limit_amount > 0),

    CONSTRAINT ck_budget_plan_alert
        CHECK (alert_ratio > 0 AND alert_ratio <= 1)
);

CREATE UNIQUE INDEX uk_budget_plan_scope
    ON budget_plan (
                    user_id,
                    budget_month,
                    COALESCE(category_id, 0)
        );


CREATE TABLE price_reference_rule
(
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    normalized_keyword VARCHAR(120)   NOT NULL,
    aliases            JSONB          NOT NULL DEFAULT '[]'::JSONB,
    category_id        BIGINT,
    region_code        VARCHAR(32),
    spending_level     VARCHAR(20),
    unit               VARCHAR(30),
    min_reasonable     NUMERIC(14, 2),
    max_reasonable     NUMERIC(14, 2) NOT NULL,
    severity           VARCHAR(20)    NOT NULL DEFAULT 'WARNING',
    enabled            BOOLEAN        NOT NULL DEFAULT TRUE,
    created_at         TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at         TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_price_rule_category
        FOREIGN KEY (category_id)
            REFERENCES category (id)
            ON DELETE RESTRICT,

    CONSTRAINT ck_price_rule_keyword
        CHECK (BTRIM(normalized_keyword) <> ''),

    CONSTRAINT ck_price_rule_spending_level
        CHECK (
            spending_level IS NULL
                OR spending_level IN (
                                      'ECONOMICAL',
                                      'STANDARD',
                                      'COMFORTABLE',
                                      'PREMIUM',
                                      'CUSTOM'
                )
            ),

    CONSTRAINT ck_price_rule_min
        CHECK (min_reasonable IS NULL OR min_reasonable >= 0),

    CONSTRAINT ck_price_rule_max
        CHECK (max_reasonable > 0),

    CONSTRAINT ck_price_rule_range
        CHECK (
            min_reasonable IS NULL
                OR max_reasonable >= min_reasonable
            ),

    CONSTRAINT ck_price_rule_severity
        CHECK (severity IN ('NOTICE', 'WARNING'))
);


CREATE TABLE monthly_prediction
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id          BIGINT         NOT NULL,
    target_month     DATE           NOT NULL,
    predicted_amount NUMERIC(14, 2) NOT NULL,
    lower_bound      NUMERIC(14, 2),
    upper_bound      NUMERIC(14, 2),
    algorithm        VARCHAR(80)    NOT NULL,
    model_version    VARCHAR(30)    NOT NULL,
    based_on_months  INTEGER        NOT NULL,
    explanation      TEXT,
    generated_at     TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_monthly_prediction_user
        FOREIGN KEY (user_id)
            REFERENCES app_user (id)
            ON DELETE CASCADE,

    CONSTRAINT uk_monthly_prediction
        UNIQUE (user_id, target_month, model_version),

    CONSTRAINT ck_monthly_prediction_month
        CHECK (EXTRACT(DAY FROM target_month) = 1),

    CONSTRAINT ck_monthly_prediction_amount
        CHECK (predicted_amount >= 0),

    CONSTRAINT ck_monthly_prediction_lower
        CHECK (lower_bound IS NULL OR lower_bound >= 0),

    CONSTRAINT ck_monthly_prediction_upper
        CHECK (upper_bound IS NULL OR upper_bound >= 0),

    CONSTRAINT ck_monthly_prediction_range
        CHECK (
            lower_bound IS NULL
                OR upper_bound IS NULL
                OR upper_bound >= lower_bound
            ),

    CONSTRAINT ck_monthly_prediction_algorithm
        CHECK (
            algorithm IN (
                          'WEIGHTED_MOVING_AVERAGE',
                          'TREND_ADJUSTED_AVERAGE',
                          'INSUFFICIENT_DATA_ESTIMATE'
                )
            ),

    CONSTRAINT ck_monthly_prediction_month_count
        CHECK (based_on_months >= 0)
);


