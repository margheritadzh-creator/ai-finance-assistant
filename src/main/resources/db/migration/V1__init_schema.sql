CREATE EXTENSION IF NOT EXISTS vector;

CREATE TABLE app_user
(
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email         VARCHAR(160) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    display_name  VARCHAR(60)  NOT NULL,
    role          VARCHAR(20)  NOT NULL DEFAULT 'USER',
    enabled       BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT ck_app_user_email
        CHECK (BTRIM(email) <> ''),

    CONSTRAINT ck_app_user_display_name
        CHECK (BTRIM(display_name) <> ''),

    CONSTRAINT ck_app_user_role
        CHECK (role IN ('USER', 'ADMIN'))
);

CREATE UNIQUE INDEX uk_app_user_email_lower
    ON app_user (LOWER(email));


CREATE TABLE user_preference
(
    id                     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id                BIGINT         NOT NULL UNIQUE,
    region_code            VARCHAR(32)    NOT NULL DEFAULT 'CN',
    region_name            VARCHAR(80)    NOT NULL DEFAULT '全国',
    price_index            NUMERIC(5, 2)  NOT NULL DEFAULT 1.00,
    spending_level         VARCHAR(20)    NOT NULL DEFAULT 'STANDARD',
    monthly_income         NUMERIC(14, 2),
    default_monthly_budget NUMERIC(14, 2),
    warning_enabled        BOOLEAN        NOT NULL DEFAULT TRUE,
    warning_level          VARCHAR(20)    NOT NULL DEFAULT 'MEDIUM',
    preferred_language     VARCHAR(10)    NOT NULL DEFAULT 'zh-CN',
    speech_language        VARCHAR(10)    NOT NULL DEFAULT 'zh-CN',
    currency               VARCHAR(3)     NOT NULL DEFAULT 'CNY',
    created_at             TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at             TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_user_preference_user
        FOREIGN KEY (user_id)
            REFERENCES app_user (id)
            ON DELETE CASCADE,

    CONSTRAINT ck_user_preference_price_index
        CHECK (price_index BETWEEN 0.10 AND 10.00),

    CONSTRAINT ck_user_preference_spending_level
        CHECK (
            spending_level IN (
                               'ECONOMICAL',
                               'STANDARD',
                               'COMFORTABLE',
                               'PREMIUM',
                               'CUSTOM'
                )
            ),

    CONSTRAINT ck_user_preference_income
        CHECK (monthly_income IS NULL OR monthly_income >= 0),

    CONSTRAINT ck_user_preference_budget
        CHECK (
            default_monthly_budget IS NULL
                OR default_monthly_budget >= 0
            ),

    CONSTRAINT ck_user_preference_warning_level
        CHECK (warning_level IN ('LOW', 'MEDIUM', 'HIGH')),

    CONSTRAINT ck_user_preference_language
        CHECK (preferred_language IN ('zh-CN', 'en-US')),

    CONSTRAINT ck_user_preference_speech_language
        CHECK (speech_language IN ('zh-CN', 'en-US')),

    CONSTRAINT ck_user_preference_currency
        CHECK (currency ~ '^[A-Z]{3}$')
    );


